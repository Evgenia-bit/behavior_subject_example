/// Клиент для работы с api авторизации.
class AuthClient {
  Future<void> logIn() {
    // Имитируем отправку запроса на вход в аккаунт.
    return Future.delayed(Duration.zero);
  }

  Future<void> logOut() {
    // Имитируем отправку запроса на выход из аккаунта.
    return Future.delayed(Duration.zero);
  }
}
